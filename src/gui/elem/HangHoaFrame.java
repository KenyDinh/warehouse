    private void okButton() {
        lb_mess.setText("");
        String mahanghoa = tf_mahanghoa.getText();
        String tenhanghoa = tf_tenhanghoa.getText();
        int gia = Integer.valueOf(tf_gia.getValue().toString());
        if (cbb_madonvi.getSelectedItem() == null) {
            lb_mess.setText("Mã đơn vị không được bỏ trống!");
            return;
        }
        if (cbb_maloaihanghoa.getSelectedItem() == null) {
            lb_mess.setText("Mã loại vật liệu không được bỏ trống!");
            return;
        }
        if (mahanghoa == null || mahanghoa.trim().isEmpty()) {
            lb_mess.setText("Mã vật liệu không được bỏ trống!");
            return;
        }
        if (tenhanghoa == null || tenhanghoa.trim().isEmpty()) {
            lb_mess.setText("Tên vật liệu không được bỏ trống!");
            return;
        }
        if (tf_mahanghoa.isEditable()) {
            HangHoa temp = new HangHoa();
            temp.setMahanghoa(mahanghoa.trim());
            if (ServiceDAOFactory.select(temp) != null) {
                lb_mess.setText("Mã Vật Liệu đã tồn tại!");
                return;
            }
        }
        HangHoa hh = new HangHoa();
        hh.setMahanghoa(mahanghoa.trim());
        hh.setTenhanghoa(tenhanghoa.trim());
        hh.setMadonvi(cbb_madonvi.getSelectedItem().toString());
        hh.setGia(gia);
        hh.setMaloaihanghoa(cbb_maloaihanghoa.getSelectedItem().toString());
        if (!ServiceDAOFactory.insertOrUpdate(hh)) {
            if (tf_mahanghoa.isEditable()) {
                lb_mess.setText("Lỗi:Không thêm được vật liệu mới!");
            } else {
                lb_mess.setText("Lỗi:Không cập nhất được thông tin vật liệu!");
            }
            return;
        }
        okCloseFrame(new HangHoa());
    }

    private void updateDonVi() {
        if (cbb_madonvi.getSelectedItem() == null) {
            return;
        }
        String madonvi = cbb_madonvi.getSelectedItem().toString();
        for (DonVi dv : listDonvi) {
            if (dv.getMadonvi().equals(madonvi)) {
                tf_tendonvi.setText(dv.getTendonvi());
                break;
            }
        }
    }

    private void updateLoaiHangHoa() {
        if (cbb_maloaihanghoa.getSelectedItem() == null) {
            return;
        }
        String maloaihanghoa = cbb_maloaihanghoa.getSelectedItem().toString();
        for (LoaiHangHoa lhh : listLoaiHangHoa) {
            if (lhh.getMaloaihanghoa().equals(maloaihanghoa)) {
                tf_tenloaihanghoa.setText(lhh.getTenloaihanghoa());
                break;
            }
        }
    }


