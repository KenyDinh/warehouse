    private void okButton() {
        lb_mess.setText("");
        String tendangnhap = tf_tendangnhap.getText();
        char[] pass = tf_pass.getPassword();
        String email = tf_email.getText();
        String diachi = tf_diachi.getText();
        String sodienthoai = tf_sdt.getText();

        if (tendangnhap == null || tendangnhap.trim().isEmpty()) {
            lb_mess.setText("Tên đăng nhập không được bỏ trống!");
            return;
        }
        if (tendangnhap.trim().equals("root")) {
            lb_mess.setText("Tên đăng nhập không hợp lệ!");
            return;
        }
        if (diachi == null || diachi.trim().isEmpty()) {
            lb_mess.setText("Địa chỉ không được bỏ trống!");
            return;
        }
        if (sodienthoai == null || sodienthoai.trim().isEmpty()) {
            lb_mess.setText("Số điện thoại không được bỏ trống!");
            return;
        }
        if (!pn_id.isVisible()) {
            if (pass == null || pass.length == 0) {
                lb_mess.setText("Mật khẩu không được bỏ trống!");
                return;
            }
            matkhau = CommonMethod.encryptedPasswordMD5(new String(pass));
        }
        if(matkhau == null){
            matkhau = CommonMethod.encryptedPasswordMD5(new String("1"));
        }
        if (email == null) {
            email = "";
        }
        String machucvu = null;
        int role = Role.DEFAULT_ROLE.getId();
        if (listChucVus != null && cbb_chucvu.getSelectedIndex() >= 0 && cbb_chucvu.getSelectedIndex() < listChucVus.size()) {
            machucvu = listChucVus.get(cbb_chucvu.getSelectedIndex()).getMachucvu();
        }
        if (listRoles != null && cbb_role.getSelectedIndex() >= 0 && cbb_role.getSelectedIndex() < listRoles.size()) {
            role = listRoles.get(cbb_role.getSelectedIndex()).getId();
        }
        if (machucvu == null) {
            lb_mess.setText("Chức vụ không hợp lệ!");
            return;
        }
        TaiKhoan tk = new TaiKhoan();
        if (pn_id.isVisible()) {
            String strId = tf_id.getText();
            if (strId == null || strId.trim().isEmpty()) {
                lb_mess.setText("Id tài khoản không hợp lệ!");
                return;
            }
            tk.setId(CommonMethod.stringToInt(strId));
        } else {
            TaiKhoan temp = new TaiKhoan();
            temp.setSelectQuery("FROM " + CommonDefine.TAIKHOAN + " WHERE username='" + tendangnhap + "'");
            if (!ServiceDAOFactory.executeQueryList(temp).isEmpty()) {
                lb_mess.setText("Tài khoản đã tồn tại!");
                return;
            }
        }
        tk.setDiachi(diachi);
        tk.setEmail(email);
        tk.setMachucvu(machucvu);
        tk.setPassword(matkhau);
        tk.setRole(role);
        tk.setSodienthoai(sodienthoai);
        tk.setUsername(tendangnhap);
        if (!ServiceDAOFactory.insertOrUpdate(tk)) {
            if (pn_id.isVisible()) {
                lb_mess.setText("Lỗi:Cập nhật thông tin thất bại!");
            } else {
                lb_mess.setText("Lỗi:Thêm thông tin thất bại!");
            }
            return;
        }
        okCloseFrame();
    }


