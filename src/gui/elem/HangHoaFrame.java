    private void initCombobox() {
        List<EntityComponent> list_dv = ServiceDAOFactory.selectList(new DonVi());
        List<EntityComponent> list_lhh = ServiceDAOFactory.selectList(new LoaiHangHoa());
        DefaultComboBoxModel<String> donviModel = new DefaultComboBoxModel<>();
        DefaultComboBoxModel<String> loaihanghoaModel = new DefaultComboBoxModel<>();
        boolean b = true;
        for (EntityComponent e : list_dv) {
            DonVi dv = (DonVi) e;
            listDonvi.add(dv);
            donviModel.addElement(dv.getMadonvi());
            if (b) {
                tf_tendonvi.setText(dv.getTendonvi());
                b = false;
            }
        }
        b = true;
        for (EntityComponent e : list_lhh) {
            LoaiHangHoa lhh = (LoaiHangHoa) e;
            listLoaiHangHoa.add(lhh);
            loaihanghoaModel.addElement(lhh.getMaloaihanghoa());
            if (b) {
                tf_tenloaihanghoa.setText(lhh.getTenloaihanghoa());
                b = false;
            }
        }
        cbb_madonvi.setModel(donviModel);
        cbb_maloaihanghoa.setModel(loaihanghoaModel);
        updateDonVi();
        updateLoaiHangHoa();
    }

    @Override
    public void initData(EntityComponent entity) {
        if (entity != null && entity instanceof HangHoa) {
            HangHoa hh = (HangHoa) entity;
            tf_mahanghoa.setText(hh.getMahanghoa());
            tf_mahanghoa.setEditable(false);
            tf_gia.setValue(hh.getGia());
            cbb_madonvi.setSelectedItem(hh.getMadonvi());
            cbb_maloaihanghoa.setSelectedItem(hh.getMaloaihanghoa());
            for (DonVi dv : listDonvi) {
                if (dv.getMadonvi().equals(hh.getMadonvi())) {
                    tf_tendonvi.setText(dv.getTendonvi());
                    break;
                }
            }
            for (LoaiHangHoa lhh : listLoaiHangHoa) {
                if (lhh.getTenloaihanghoa().equals(hh.getMahanghoa())) {
                    tf_tenloaihanghoa.setText(lhh.getTenloaihanghoa());
                    break;
                }
            }
            tf_tenhanghoa.setText(hh.getTenhanghoa());
            lb_title.setText("Cập Nhật Vật Liệu");
            btn_ok.setText("Cập Nhập");
        }
    }

