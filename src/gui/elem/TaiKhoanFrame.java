    private void initConfig() {
        listChucVus = new ArrayList<>();
        listRoles = new ArrayList<>();
        DefaultComboBoxModel<String> chucvuModel = new DefaultComboBoxModel<>();
        DefaultComboBoxModel<String> roleModel = new DefaultComboBoxModel<>();
        ChucVu cv = new ChucVu();
        List<EntityComponent> list = ServiceDAOFactory.selectList(cv);
        for (EntityComponent e : list) {
            ChucVu c = (ChucVu) e;
            listChucVus.add(c);
            chucvuModel.addElement(c.getTenchucvu());
        }
        for (Role role : Role.values()) {
            listRoles.add(role);
            roleModel.addElement(role.getName());
        }
        cbb_chucvu.setModel(chucvuModel);
        cbb_role.setModel(roleModel);
    }

    @Override
    public void initData(EntityComponent entity) {
        if (entity != null && entity instanceof TaiKhoan) {
            TaiKhoan tk = (TaiKhoan) entity;
            lb_title.setText("Cập Nhật Thông Tin Người Dùng");
            btn_ok.setText("Cập Nhập");
            pn_id.setVisible(true);
            tf_id.setText(String.valueOf(tk.getId()));
            tf_tendangnhap.setText(tk.getUsername());
            tf_tendangnhap.setEditable(false);
            tf_pass.setVisible(false);
            lb_pass.setVisible(false);
            matkhau = tk.getPassword();
            tf_diachi.setText(tk.getDiachi());
            tf_email.setText(tk.getEmail());
            tf_sdt.setText(tk.getSodienthoai());
            for (ChucVu cv : listChucVus) {
                if (cv.getMachucvu().equals(tk.getMachucvu())) {
                    cbb_chucvu.setSelectedItem(cv.getTenchucvu());
                    break;
                }
            }
            for (Role role : listRoles) {
                if (role.getId() == tk.getRole()) {
                    cbb_role.setSelectedItem(role.getName());
                    break;
                }
            }
        }
    }

